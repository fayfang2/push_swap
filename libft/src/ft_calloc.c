#include "../include/libft.h"

void	*ft_calloc(size_t nmemb, size_t size)
{
	void	*p;
	size_t	max;

	max = nmemb * size;
	if (nmemb == 0 || size == 0)
		return (malloc(0));
	if ((nmemb * size) != max)
		return (NULL);
	p = malloc(nmemb * size);
	if (!p)
		return (NULL);
	ft_bzero(p, nmemb * size);
	return (p);
}
